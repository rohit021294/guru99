package javaaaa;

public class ArraySumOfElement {
    public static void main(String[] args)
    {
        int[] arr=new int[]{11,12,13,14,15,16};
        int sum=0;
        for(int i=0;i<arr.length;i++)
        {
            sum=sum+arr[i];
        }
        System.out.println(sum);






















    }
}
